// We need a separate descriptor set for the present pass that samples computeOutputImage.
// We reuse overlayDSLayout. We'll allocate from the same pool but we need to increase pool size.
// Actually we already allocated 4 sets. Let's create a second pool for the present DS.

static VkDescriptorPool presentDescPool;
static VkDescriptorSet  presentDS;

static void createPresentDescriptor(VulkanContext& ctx, RenderResources& res) {
    VkDescriptorPoolSize poolSize = { VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER, 1 };
    VkDescriptorPoolCreateInfo pci = { VK_STRUCTURE_TYPE_DESCRIPTOR_POOL_CREATE_INFO };
    pci.maxSets = 1;
    pci.poolSizeCount = 1;
    pci.pPoolSizes = &poolSize;
    VK_CHECK(vkCreateDescriptorPool(ctx.device, &pci, nullptr, &presentDescPool));

    VkDescriptorSetAllocateInfo ai = { VK_STRUCTURE_TYPE_DESCRIPTOR_SET_ALLOCATE_INFO };
    ai.descriptorPool = presentDescPool;
    ai.descriptorSetCount = 1;
    ai.pSetLayouts = &res.overlayDSLayout;
    VK_CHECK(vkAllocateDescriptorSets(ctx.device, &ai, &presentDS));

    VkDescriptorImageInfo imgInfo = { res.sampler, res.computeOutputView, VK_IMAGE_LAYOUT_SHADER_READ_ONLY_OPTIMAL };
    VkWriteDescriptorSet w = { VK_STRUCTURE_TYPE_WRITE_DESCRIPTOR_SET };
    w.dstSet = presentDS;
    w.dstBinding = 0;
    w.descriptorCount = 1;
    w.descriptorType = VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER;
    w.pImageInfo = &imgInfo;
    vkUpdateDescriptorSets(ctx.device, 1, &w, 0, nullptr);
}

// ---------------------------------------------------------------------------
// Update uniforms
// ---------------------------------------------------------------------------

static void updateUniforms(RenderResources& res, uint32_t frame) {
    float angle = (float)frame * (2.0f * PI / 120.0f);
    float camDist = 4.0f;
    float ex = sinf(angle) * camDist;
    float ez = cosf(angle) * camDist;

    Mat4 view = mat4LookAt(ex, 2.0f, ez, 0, 0, 0, 0, 1, 0);
    Mat4 proj = mat4Perspective(PI / 4.0f, (float)WINDOW_WIDTH / (float)WINDOW_HEIGHT, 0.1f, 100.0f);
    Mat4 model = mat4Identity();
    Mat4 mvp = mat4Multiply(proj, mat4Multiply(view, model));

    // Slot 0: shadow pass (same MVP for simplicity)
    {
        UniformData* u = (UniformData*)((uint8_t*)res.uboMapped + 0);
        memcpy(u->mvp, mvp.m, 64);
        memcpy(u->model, model.m, 64);
        u->lightPos[0] = 3.0f; u->lightPos[1] = 5.0f; u->lightPos[2] = 3.0f; u->lightPos[3] = 1.0f;
        u->tintColor[0] = 1.0f; u->tintColor[1] = 1.0f; u->tintColor[2] = 1.0f; u->tintColor[3] = 1.0f;
    }
    // Slot 1: geometry pass
    {
        UniformData* u = (UniformData*)((uint8_t*)res.uboMapped + UBO_ALIGN);
        memcpy(u->mvp, mvp.m, 64);
        memcpy(u->model, model.m, 64);
        u->lightPos[0] = 3.0f; u->lightPos[1] = 5.0f; u->lightPos[2] = 3.0f; u->lightPos[3] = 1.0f;
        u->tintColor[0] = 1.0f; u->tintColor[1] = 0.9f; u->tintColor[2] = 0.8f; u->tintColor[3] = 1.0f;
    }
    // Slot 2: reserved (unused for now)
    {
        UniformData* u = (UniformData*)((uint8_t*)res.uboMapped + UBO_ALIGN * 2);
        memcpy(u->mvp, mvp.m, 64);
        memcpy(u->model, model.m, 64);
        u->lightPos[0] = 0; u->lightPos[1] = 0; u->lightPos[2] = 0; u->lightPos[3] = 0;
        u->tintColor[0] = 1; u->tintColor[1] = 1; u->tintColor[2] = 1; u->tintColor[3] = 1;
    }
}

// ---------------------------------------------------------------------------
// Record command buffer
// ---------------------------------------------------------------------------

static void recordCommandBuffer(VulkanContext& ctx, RenderResources& res, uint32_t imageIndex) {
    VkCommandBuffer cb = ctx.commandBuffer;
    VkCommandBufferBeginInfo bi = { VK_STRUCTURE_TYPE_COMMAND_BUFFER_BEGIN_INFO };
    VK_CHECK(vkBeginCommandBuffer(cb, &bi));

    // ---- Pass 1: Shadow ----
    {
        VkClearValue clear = {};
        clear.depthStencil = { 1.0f, 0 };
        VkRenderPassBeginInfo rpbi = { VK_STRUCTURE_TYPE_RENDER_PASS_BEGIN_INFO };
        rpbi.renderPass = res.shadowRenderPass;
        rpbi.framebuffer = res.shadowFramebuffer;
        rpbi.renderArea = { {0,0}, {SHADOW_SIZE, SHADOW_SIZE} };
        rpbi.clearValueCount = 1;
        rpbi.pClearValues = &clear;
        vkCmdBeginRenderPass(cb, &rpbi, VK_SUBPASS_CONTENTS_INLINE);

        VkViewport vp = { 0, 0, (float)SHADOW_SIZE, (float)SHADOW_SIZE, 0, 1 };
        VkRect2D sc = { {0,0}, {SHADOW_SIZE, SHADOW_SIZE} };
        vkCmdSetViewport(cb, 0, 1, &vp);
        vkCmdSetScissor(cb, 0, 1, &sc);

        vkCmdBindPipeline(cb, VK_PIPELINE_BIND_POINT_GRAPHICS, res.shadowPipeline);
        uint32_t dynOffset = 0;
        vkCmdBindDescriptorSets(cb, VK_PIPELINE_BIND_POINT_GRAPHICS, res.shadowPipeLayout, 0, 1, &res.shadowDS, 1, &dynOffset);

        VkDeviceSize offset = 0;
        vkCmdBindVertexBuffers(cb, 0, 1, &res.vertexBuffer, &offset);
        vkCmdBindIndexBuffer(cb, res.indexBuffer, 0, VK_INDEX_TYPE_UINT16);
        vkCmdDrawIndexed(cb, 36, 1, 0, 0, 0);

        vkCmdEndRenderPass(cb);
    }

    // ---- Pass 2: Geometry ----
    {
        VkClearValue clears[2] = {};
        clears[0].color = {{0.05f, 0.05f, 0.1f, 1.0f}};
        clears[1].depthStencil = { 1.0f, 0 };
        VkRenderPassBeginInfo rpbi = { VK_STRUCTURE_TYPE_RENDER_PASS_BEGIN_INFO };
        rpbi.renderPass = res.geomRenderPass;
        rpbi.framebuffer = res.geomFramebuffer;
        rpbi.renderArea = { {0,0}, {WINDOW_WIDTH, WINDOW_HEIGHT} };
        rpbi.clearValueCount = 2;
        rpbi.pClearValues = clears;
        vkCmdBeginRenderPass(cb, &rpbi, VK_SUBPASS_CONTENTS_INLINE);

        VkViewport vp = { 0, 0, (float)WINDOW_WIDTH, (float)WINDOW_HEIGHT, 0, 1 };
        VkRect2D sc = { {0,0}, {WINDOW_WIDTH, WINDOW_HEIGHT} };
        vkCmdSetViewport(cb, 0, 1, &vp);
        vkCmdSetScissor(cb, 0, 1, &sc);

        // Draw cube
        vkCmdBindPipeline(cb, VK_PIPELINE_BIND_POINT_GRAPHICS, res.geomCubePipeline);
        uint32_t dynOffset = UBO_ALIGN;
        vkCmdBindDescriptorSets(cb, VK_PIPELINE_BIND_POINT_GRAPHICS, res.geomPipeLayout, 0, 1, &res.geomDS, 1, &dynOffset);
        VkDeviceSize offset = 0;
        vkCmdBindVertexBuffers(cb, 0, 1, &res.vertexBuffer, &offset);
        vkCmdBindIndexBuffer(cb, res.indexBuffer, 0, VK_INDEX_TYPE_UINT16);
        vkCmdDrawIndexed(cb, 36, 1, 0, 0, 0);

        // Draw overlay quad (covers same area for pixel_history)
        vkCmdBindPipeline(cb, VK_PIPELINE_BIND_POINT_GRAPHICS, res.overlayPipeline);
        vkCmdBindDescriptorSets(cb, VK_PIPELINE_BIND_POINT_GRAPHICS, res.overlayPipeLayout, 0, 1, &res.overlayDS, 0, nullptr);
        vkCmdDraw(cb, 4, 1, 0, 0);

        vkCmdEndRenderPass(cb);
    }

    // ---- Pass 3: Compute ----
    {
        // geomColorImage is already in SHADER_READ_ONLY_OPTIMAL from render pass final layout.
        // computeOutputImage needs to be in GENERAL for storage writes.
        transitionImageLayout(cb, res.computeOutputImage,
                              VK_IMAGE_LAYOUT_SHADER_READ_ONLY_OPTIMAL, VK_IMAGE_LAYOUT_GENERAL,
                              VK_IMAGE_ASPECT_COLOR_BIT,
                              VK_PIPELINE_STAGE_FRAGMENT_SHADER_BIT, VK_PIPELINE_STAGE_COMPUTE_SHADER_BIT,
                              VK_ACCESS_SHADER_READ_BIT, VK_ACCESS_SHADER_WRITE_BIT);

        vkCmdBindPipeline(cb, VK_PIPELINE_BIND_POINT_COMPUTE, res.computePipeline);
        vkCmdBindDescriptorSets(cb, VK_PIPELINE_BIND_POINT_COMPUTE, res.computePipeLayout, 0, 1, &res.computeDS, 0, nullptr);
        vkCmdDispatch(cb, (WINDOW_WIDTH + 15) / 16, (WINDOW_HEIGHT + 15) / 16, 1);

        // Transition compute output to SHADER_READ_ONLY for present pass
        transitionImageLayout(cb, res.computeOutputImage,
                              VK_IMAGE_LAYOUT_GENERAL, VK_IMAGE_LAYOUT_SHADER_READ_ONLY_OPTIMAL,
                              VK_IMAGE_ASPECT_COLOR_BIT,
                              VK_PIPELINE_STAGE_COMPUTE_SHADER_BIT, VK_PIPELINE_STAGE_FRAGMENT_SHADER_BIT,
                              VK_ACCESS_SHADER_WRITE_BIT, VK_ACCESS_SHADER_READ_BIT);
    }

    // ---- Pass 4: Present ----
    {
        VkClearValue clear = {};
        clear.color = {{0, 0, 0, 1}};
        VkRenderPassBeginInfo rpbi = { VK_STRUCTURE_TYPE_RENDER_PASS_BEGIN_INFO };
        rpbi.renderPass = res.presentRenderPass;
        rpbi.framebuffer = res.presentFramebuffers[imageIndex];
        rpbi.renderArea = { {0,0}, {WINDOW_WIDTH, WINDOW_HEIGHT} };
        rpbi.clearValueCount = 1;
        rpbi.pClearValues = &clear;
        vkCmdBeginRenderPass(cb, &rpbi, VK_SUBPASS_CONTENTS_INLINE);

        VkViewport vp = { 0, 0, (float)WINDOW_WIDTH, (float)WINDOW_HEIGHT, 0, 1 };
        VkRect2D sc = { {0,0}, {WINDOW_WIDTH, WINDOW_HEIGHT} };
        vkCmdSetViewport(cb, 0, 1, &vp);
        vkCmdSetScissor(cb, 0, 1, &sc);

        vkCmdBindPipeline(cb, VK_PIPELINE_BIND_POINT_GRAPHICS, res.presentPipeline);
        vkCmdBindDescriptorSets(cb, VK_PIPELINE_BIND_POINT_GRAPHICS, res.presentPipeLayout, 0, 1, &presentDS, 0, nullptr);
        vkCmdDraw(cb, 4, 1, 0, 0);

        vkCmdEndRenderPass(cb);
    }

    VK_CHECK(vkEndCommandBuffer(cb));
}

