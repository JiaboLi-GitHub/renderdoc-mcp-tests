// ---------------------------------------------------------------------------
// Graphics pipelines
// ---------------------------------------------------------------------------

static void createGraphicsPipelines(VulkanContext& ctx, RenderResources& res) {
    // Shared state builders
    VkPipelineInputAssemblyStateCreateInfo iaState = { VK_STRUCTURE_TYPE_PIPELINE_INPUT_ASSEMBLY_STATE_CREATE_INFO };
    iaState.topology = VK_PRIMITIVE_TOPOLOGY_TRIANGLE_LIST;

    VkPipelineViewportStateCreateInfo vpState = { VK_STRUCTURE_TYPE_PIPELINE_VIEWPORT_STATE_CREATE_INFO };
    vpState.viewportCount = 1;
    vpState.scissorCount = 1;

    VkDynamicState dynStates[] = { VK_DYNAMIC_STATE_VIEWPORT, VK_DYNAMIC_STATE_SCISSOR };
    VkPipelineDynamicStateCreateInfo dynState = { VK_STRUCTURE_TYPE_PIPELINE_DYNAMIC_STATE_CREATE_INFO };
    dynState.dynamicStateCount = 2;
    dynState.pDynamicStates = dynStates;

    VkPipelineMultisampleStateCreateInfo msState = { VK_STRUCTURE_TYPE_PIPELINE_MULTISAMPLE_STATE_CREATE_INFO };
    msState.rasterizationSamples = VK_SAMPLE_COUNT_1_BIT;

    // Vertex input for cube
    VkVertexInputBindingDescription vertBinding = { 0, sizeof(Vertex), VK_VERTEX_INPUT_RATE_VERTEX };
    VkVertexInputAttributeDescription vertAttrs[3] = {};
    vertAttrs[0] = { 0, 0, VK_FORMAT_R32G32B32_SFLOAT, 0 };                    // position
    vertAttrs[1] = { 1, 0, VK_FORMAT_R32G32B32_SFLOAT, offsetof(Vertex, normal) }; // normal
    vertAttrs[2] = { 2, 0, VK_FORMAT_R32G32_SFLOAT, offsetof(Vertex, texcoord) };  // texcoord
    VkPipelineVertexInputStateCreateInfo viCube = { VK_STRUCTURE_TYPE_PIPELINE_VERTEX_INPUT_STATE_CREATE_INFO };
    viCube.vertexBindingDescriptionCount = 1;
    viCube.pVertexBindingDescriptions = &vertBinding;
    viCube.vertexAttributeDescriptionCount = 3;
    viCube.pVertexAttributeDescriptions = vertAttrs;

    // Empty vertex input for fullscreen quad shaders
    VkPipelineVertexInputStateCreateInfo viEmpty = { VK_STRUCTURE_TYPE_PIPELINE_VERTEX_INPUT_STATE_CREATE_INFO };

    // Rasterizer: back cull
    VkPipelineRasterizationStateCreateInfo rasterCullBack = { VK_STRUCTURE_TYPE_PIPELINE_RASTERIZATION_STATE_CREATE_INFO };
    rasterCullBack.polygonMode = VK_POLYGON_MODE_FILL;
    rasterCullBack.cullMode = VK_CULL_MODE_BACK_BIT;
    rasterCullBack.frontFace = VK_FRONT_FACE_COUNTER_CLOCKWISE;
    rasterCullBack.lineWidth = 1.0f;

    // Rasterizer: no cull
    VkPipelineRasterizationStateCreateInfo rasterNoCull = { VK_STRUCTURE_TYPE_PIPELINE_RASTERIZATION_STATE_CREATE_INFO };
    rasterNoCull.polygonMode = VK_POLYGON_MODE_FILL;
    rasterNoCull.cullMode = VK_CULL_MODE_NONE;
    rasterNoCull.frontFace = VK_FRONT_FACE_COUNTER_CLOCKWISE;
    rasterNoCull.lineWidth = 1.0f;

    // Depth: on
    VkPipelineDepthStencilStateCreateInfo depthOn = { VK_STRUCTURE_TYPE_PIPELINE_DEPTH_STENCIL_STATE_CREATE_INFO };
    depthOn.depthTestEnable = VK_TRUE;
    depthOn.depthWriteEnable = VK_TRUE;
    depthOn.depthCompareOp = VK_COMPARE_OP_LESS;

    // Depth: off
    VkPipelineDepthStencilStateCreateInfo depthOff = { VK_STRUCTURE_TYPE_PIPELINE_DEPTH_STENCIL_STATE_CREATE_INFO };

    // Color blend: no blend
    VkPipelineColorBlendAttachmentState noBlend = {};
    noBlend.colorWriteMask = VK_COLOR_COMPONENT_R_BIT | VK_COLOR_COMPONENT_G_BIT | VK_COLOR_COMPONENT_B_BIT | VK_COLOR_COMPONENT_A_BIT;

    // Color blend: alpha blend
    VkPipelineColorBlendAttachmentState alphaBlend = {};
    alphaBlend.blendEnable = VK_TRUE;
    alphaBlend.srcColorBlendFactor = VK_BLEND_FACTOR_SRC_ALPHA;
    alphaBlend.dstColorBlendFactor = VK_BLEND_FACTOR_ONE_MINUS_SRC_ALPHA;
    alphaBlend.colorBlendOp = VK_BLEND_OP_ADD;
    alphaBlend.srcAlphaBlendFactor = VK_BLEND_FACTOR_ONE;
    alphaBlend.dstAlphaBlendFactor = VK_BLEND_FACTOR_ZERO;
    alphaBlend.alphaBlendOp = VK_BLEND_OP_ADD;
    alphaBlend.colorWriteMask = VK_COLOR_COMPONENT_R_BIT | VK_COLOR_COMPONENT_G_BIT | VK_COLOR_COMPONENT_B_BIT | VK_COLOR_COMPONENT_A_BIT;

    VkPipelineColorBlendStateCreateInfo cbNoBlend = { VK_STRUCTURE_TYPE_PIPELINE_COLOR_BLEND_STATE_CREATE_INFO };
    cbNoBlend.attachmentCount = 1;
    cbNoBlend.pAttachments = &noBlend;

    VkPipelineColorBlendStateCreateInfo cbAlpha = { VK_STRUCTURE_TYPE_PIPELINE_COLOR_BLEND_STATE_CREATE_INFO };
    cbAlpha.attachmentCount = 1;
    cbAlpha.pAttachments = &alphaBlend;

    // No color blend state for shadow (no color attachments)
    VkPipelineColorBlendStateCreateInfo cbNone = { VK_STRUCTURE_TYPE_PIPELINE_COLOR_BLEND_STATE_CREATE_INFO };
    cbNone.attachmentCount = 0;

    // Shader modules
    VkShaderModule shadowVertMod = createShaderModule(ctx.device, shadow_vert_spv, shadow_vert_spv_size);
    VkShaderModule geomVertMod   = createShaderModule(ctx.device, geometry_vert_spv, geometry_vert_spv_size);
    VkShaderModule geomFragMod   = createShaderModule(ctx.device, geometry_frag_spv, geometry_frag_spv_size);
    VkShaderModule overlayVertMod = createShaderModule(ctx.device, overlay_vert_spv, overlay_vert_spv_size);
    VkShaderModule overlayFragMod = createShaderModule(ctx.device, overlay_frag_spv, overlay_frag_spv_size);

    // 1. Shadow pipeline
    {
        VkPipelineShaderStageCreateInfo stages[1] = {};
        stages[0].sType = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO;
        stages[0].stage = VK_SHADER_STAGE_VERTEX_BIT;
        stages[0].module = shadowVertMod;
        stages[0].pName = "main";

        VkGraphicsPipelineCreateInfo ci = { VK_STRUCTURE_TYPE_GRAPHICS_PIPELINE_CREATE_INFO };
        ci.stageCount = 1;
        ci.pStages = stages;
        ci.pVertexInputState = &viCube;
        ci.pInputAssemblyState = &iaState;
        ci.pViewportState = &vpState;
        ci.pRasterizationState = &rasterCullBack;
        ci.pMultisampleState = &msState;
        ci.pDepthStencilState = &depthOn;
        ci.pColorBlendState = &cbNone;
        ci.pDynamicState = &dynState;
        ci.layout = res.shadowPipeLayout;
        ci.renderPass = res.shadowRenderPass;
        VK_CHECK(vkCreateGraphicsPipelines(ctx.device, VK_NULL_HANDLE, 1, &ci, nullptr, &res.shadowPipeline));
    }
    // 2. Geometry cube pipeline
    {
        VkPipelineShaderStageCreateInfo stages[2] = {};
        stages[0].sType = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO;
        stages[0].stage = VK_SHADER_STAGE_VERTEX_BIT;
        stages[0].module = geomVertMod;
        stages[0].pName = "main";
        stages[1].sType = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO;
        stages[1].stage = VK_SHADER_STAGE_FRAGMENT_BIT;
        stages[1].module = geomFragMod;
        stages[1].pName = "main";

        VkGraphicsPipelineCreateInfo ci = { VK_STRUCTURE_TYPE_GRAPHICS_PIPELINE_CREATE_INFO };
        ci.stageCount = 2;
        ci.pStages = stages;
        ci.pVertexInputState = &viCube;
        ci.pInputAssemblyState = &iaState;
        ci.pViewportState = &vpState;
        ci.pRasterizationState = &rasterCullBack;
        ci.pMultisampleState = &msState;
        ci.pDepthStencilState = &depthOn;
        ci.pColorBlendState = &cbNoBlend;
        ci.pDynamicState = &dynState;
        ci.layout = res.geomPipeLayout;
        ci.renderPass = res.geomRenderPass;
        VK_CHECK(vkCreateGraphicsPipelines(ctx.device, VK_NULL_HANDLE, 1, &ci, nullptr, &res.geomCubePipeline));
    }
    // 3. Overlay pipeline (alpha blend, no depth, no vertex input)
    {
        VkPipelineShaderStageCreateInfo stages[2] = {};
        stages[0].sType = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO;
        stages[0].stage = VK_SHADER_STAGE_VERTEX_BIT;
        stages[0].module = overlayVertMod;
        stages[0].pName = "main";
        stages[1].sType = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO;
        stages[1].stage = VK_SHADER_STAGE_FRAGMENT_BIT;
        stages[1].module = overlayFragMod;
        stages[1].pName = "main";

        VkGraphicsPipelineCreateInfo ci = { VK_STRUCTURE_TYPE_GRAPHICS_PIPELINE_CREATE_INFO };
        ci.stageCount = 2;
        ci.pStages = stages;
        ci.pVertexInputState = &viEmpty;
        ci.pInputAssemblyState = &iaState;
        ci.pViewportState = &vpState;
        ci.pRasterizationState = &rasterNoCull;
        ci.pMultisampleState = &msState;
        ci.pDepthStencilState = &depthOff;
        ci.pColorBlendState = &cbAlpha;
        ci.pDynamicState = &dynState;
        ci.layout = res.overlayPipeLayout;
        ci.renderPass = res.geomRenderPass;
        VK_CHECK(vkCreateGraphicsPipelines(ctx.device, VK_NULL_HANDLE, 1, &ci, nullptr, &res.overlayPipeline));
    }
    // 4. Present pipeline (no blend, no depth, same overlay shaders)
    {
        VkPipelineShaderStageCreateInfo stages[2] = {};
        stages[0].sType = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO;
        stages[0].stage = VK_SHADER_STAGE_VERTEX_BIT;
        stages[0].module = overlayVertMod;
        stages[0].pName = "main";
        stages[1].sType = VK_STRUCTURE_TYPE_PIPELINE_SHADER_STAGE_CREATE_INFO;
        stages[1].stage = VK_SHADER_STAGE_FRAGMENT_BIT;
        stages[1].module = overlayFragMod;
        stages[1].pName = "main";

        VkGraphicsPipelineCreateInfo ci = { VK_STRUCTURE_TYPE_GRAPHICS_PIPELINE_CREATE_INFO };
        ci.stageCount = 2;
        ci.pStages = stages;
        ci.pVertexInputState = &viEmpty;
        ci.pInputAssemblyState = &iaState;
        ci.pViewportState = &vpState;
        ci.pRasterizationState = &rasterNoCull;
        ci.pMultisampleState = &msState;
        ci.pDepthStencilState = &depthOff;
        ci.pColorBlendState = &cbNoBlend;
        ci.pDynamicState = &dynState;
        ci.layout = res.presentPipeLayout;
        ci.renderPass = res.presentRenderPass;
        VK_CHECK(vkCreateGraphicsPipelines(ctx.device, VK_NULL_HANDLE, 1, &ci, nullptr, &res.presentPipeline));
    }

    vkDestroyShaderModule(ctx.device, shadowVertMod, nullptr);
    vkDestroyShaderModule(ctx.device, geomVertMod, nullptr);
    vkDestroyShaderModule(ctx.device, geomFragMod, nullptr);
    vkDestroyShaderModule(ctx.device, overlayVertMod, nullptr);
    vkDestroyShaderModule(ctx.device, overlayFragMod, nullptr);
}

