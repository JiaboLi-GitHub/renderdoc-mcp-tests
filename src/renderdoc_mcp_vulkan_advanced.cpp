struct Mat4 { float m[16]; };

static Mat4 mat4Identity() {
    Mat4 r = {};
    r.m[0] = r.m[5] = r.m[10] = r.m[15] = 1.0f;
    return r;
}

static Mat4 mat4Multiply(const Mat4& a, const Mat4& b) {
    Mat4 r = {};
    for (int i = 0; i < 4; i++)
        for (int j = 0; j < 4; j++)
            for (int k = 0; k < 4; k++)
                r.m[j * 4 + i] += a.m[k * 4 + i] * b.m[j * 4 + k];
    return r;
}

static Mat4 mat4Perspective(float fovY, float aspect, float zn, float zf) {
    float tanHalf = tanf(fovY * 0.5f);
    Mat4 r = {};
    r.m[0]  = 1.0f / (aspect * tanHalf);
    r.m[5]  = -1.0f / tanHalf; // Vulkan Y-flip
    r.m[10] = zf / (zn - zf);
    r.m[11] = -1.0f;
    r.m[14] = (zn * zf) / (zn - zf);
    return r;
}

static Mat4 mat4LookAt(float ex, float ey, float ez, float cx, float cy, float cz, float ux, float uy, float uz) {
    float fx = cx - ex, fy = cy - ey, fz = cz - ez;
    float fl = sqrtf(fx*fx + fy*fy + fz*fz);
    fx /= fl; fy /= fl; fz /= fl;
    float sx = fy*uz - fz*uy, sy = fz*ux - fx*uz, sz = fx*uy - fy*ux;
    float sl = sqrtf(sx*sx + sy*sy + sz*sz);
    sx /= sl; sy /= sl; sz /= sl;
    float ux2 = sy*fz - sz*fy, uy2 = sz*fx - sx*fz, uz2 = sx*fy - sy*fx;
    Mat4 r = {};
    r.m[0] = sx;  r.m[4] = sy;  r.m[8]  = sz;  r.m[12] = -(sx*ex + sy*ey + sz*ez);
    r.m[1] = ux2; r.m[5] = uy2; r.m[9]  = uz2; r.m[13] = -(ux2*ex + uy2*ey + uz2*ez);
    r.m[2] = -fx; r.m[6] = -fy; r.m[10] = -fz; r.m[14] = (fx*ex + fy*ey + fz*ez);
    r.m[3] = 0; r.m[7] = 0; r.m[11] = 0; r.m[15] = 1.0f;
    return r;
}

static Mat4 mat4RotationY(float angle) {
    Mat4 r = mat4Identity();
    float c = cosf(angle), s = sinf(angle);
    r.m[0] = c;  r.m[8]  = s;
    r.m[2] = -s; r.m[10] = c;
    return r;
}

