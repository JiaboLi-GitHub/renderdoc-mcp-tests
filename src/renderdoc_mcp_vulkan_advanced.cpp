static void createFramebuffers(VulkanContext& ctx, RenderResources& res) {
    // Shadow
    {
        VkFramebufferCreateInfo ci = { VK_STRUCTURE_TYPE_FRAMEBUFFER_CREATE_INFO };
        ci.renderPass = res.shadowRenderPass;
        ci.attachmentCount = 1;
        ci.pAttachments = &res.shadowDepthView;
        ci.width = SHADOW_SIZE;
        ci.height = SHADOW_SIZE;
        ci.layers = 1;
        VK_CHECK(vkCreateFramebuffer(ctx.device, &ci, nullptr, &res.shadowFramebuffer));
    }
    // Geometry
    {
        VkImageView views[2] = { res.geomColorView, res.geomDepthView };
        VkFramebufferCreateInfo ci = { VK_STRUCTURE_TYPE_FRAMEBUFFER_CREATE_INFO };
        ci.renderPass = res.geomRenderPass;
        ci.attachmentCount = 2;
        ci.pAttachments = views;
        ci.width = WINDOW_WIDTH;
        ci.height = WINDOW_HEIGHT;
        ci.layers = 1;
        VK_CHECK(vkCreateFramebuffer(ctx.device, &ci, nullptr, &res.geomFramebuffer));
    }
    // Present (one per swapchain image)
    res.presentFramebuffers.resize(ctx.swapchainImageCount);
    for (uint32_t i = 0; i < ctx.swapchainImageCount; i++) {
        VkFramebufferCreateInfo ci = { VK_STRUCTURE_TYPE_FRAMEBUFFER_CREATE_INFO };
        ci.renderPass = res.presentRenderPass;
        ci.attachmentCount = 1;
        ci.pAttachments = &ctx.swapchainViews[i];
        ci.width = WINDOW_WIDTH;
        ci.height = WINDOW_HEIGHT;
        ci.layers = 1;
        VK_CHECK(vkCreateFramebuffer(ctx.device, &ci, nullptr, &res.presentFramebuffers[i]));
    }
}

// ---------------------------------------------------------------------------
// Descriptor set layouts, pool, sets
// ---------------------------------------------------------------------------

static void createDescriptors(VulkanContext& ctx, RenderResources& res) {
    // Shadow DSLayout: dynamic UBO at binding 0
    {
        VkDescriptorSetLayoutBinding binding = {};
        binding.binding = 0;
        binding.descriptorType = VK_DESCRIPTOR_TYPE_UNIFORM_BUFFER_DYNAMIC;
        binding.descriptorCount = 1;
        binding.stageFlags = VK_SHADER_STAGE_VERTEX_BIT;
        VkDescriptorSetLayoutCreateInfo ci = { VK_STRUCTURE_TYPE_DESCRIPTOR_SET_LAYOUT_CREATE_INFO };
        ci.bindingCount = 1;
        ci.pBindings = &binding;
        VK_CHECK(vkCreateDescriptorSetLayout(ctx.device, &ci, nullptr, &res.shadowDSLayout));
    }
    // Geometry DSLayout: dynamic UBO (binding 0) + combined image sampler (binding 1)
    {
        VkDescriptorSetLayoutBinding bindings[2] = {};
        bindings[0].binding = 0;
        bindings[0].descriptorType = VK_DESCRIPTOR_TYPE_UNIFORM_BUFFER_DYNAMIC;
        bindings[0].descriptorCount = 1;
        bindings[0].stageFlags = VK_SHADER_STAGE_VERTEX_BIT | VK_SHADER_STAGE_FRAGMENT_BIT;
        bindings[1].binding = 1;
        bindings[1].descriptorType = VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER;
        bindings[1].descriptorCount = 1;
        bindings[1].stageFlags = VK_SHADER_STAGE_FRAGMENT_BIT;
        VkDescriptorSetLayoutCreateInfo ci = { VK_STRUCTURE_TYPE_DESCRIPTOR_SET_LAYOUT_CREATE_INFO };
        ci.bindingCount = 2;
        ci.pBindings = bindings;
        VK_CHECK(vkCreateDescriptorSetLayout(ctx.device, &ci, nullptr, &res.geomDSLayout));
    }
    // Overlay DSLayout: combined image sampler (binding 0)
    {
        VkDescriptorSetLayoutBinding binding = {};
        binding.binding = 0;
        binding.descriptorType = VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER;
        binding.descriptorCount = 1;
        binding.stageFlags = VK_SHADER_STAGE_FRAGMENT_BIT;
        VkDescriptorSetLayoutCreateInfo ci = { VK_STRUCTURE_TYPE_DESCRIPTOR_SET_LAYOUT_CREATE_INFO };
        ci.bindingCount = 1;
        ci.pBindings = &binding;
        VK_CHECK(vkCreateDescriptorSetLayout(ctx.device, &ci, nullptr, &res.overlayDSLayout));
    }
    // Compute DSLayout: sampled image (binding 0) + storage image (binding 1)
    {
        VkDescriptorSetLayoutBinding bindings[2] = {};
        bindings[0].binding = 0;
        bindings[0].descriptorType = VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER;
        bindings[0].descriptorCount = 1;
        bindings[0].stageFlags = VK_SHADER_STAGE_COMPUTE_BIT;
        bindings[1].binding = 1;
        bindings[1].descriptorType = VK_DESCRIPTOR_TYPE_STORAGE_IMAGE;
        bindings[1].descriptorCount = 1;
        bindings[1].stageFlags = VK_SHADER_STAGE_COMPUTE_BIT;
        VkDescriptorSetLayoutCreateInfo ci = { VK_STRUCTURE_TYPE_DESCRIPTOR_SET_LAYOUT_CREATE_INFO };
        ci.bindingCount = 2;
        ci.pBindings = bindings;
        VK_CHECK(vkCreateDescriptorSetLayout(ctx.device, &ci, nullptr, &res.computeDSLayout));
    }

    // Descriptor pool
    VkDescriptorPoolSize poolSizes[] = {
        { VK_DESCRIPTOR_TYPE_UNIFORM_BUFFER_DYNAMIC, 2 },
        { VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER, 4 },
        { VK_DESCRIPTOR_TYPE_STORAGE_IMAGE, 1 },
    };
    VkDescriptorPoolCreateInfo pci = { VK_STRUCTURE_TYPE_DESCRIPTOR_POOL_CREATE_INFO };
    pci.maxSets = 4;
    pci.poolSizeCount = 3;
    pci.pPoolSizes = poolSizes;
    VK_CHECK(vkCreateDescriptorPool(ctx.device, &pci, nullptr, &res.descriptorPool));

    // Allocate sets
    VkDescriptorSetLayout layouts[4] = { res.shadowDSLayout, res.geomDSLayout, res.overlayDSLayout, res.computeDSLayout };
    VkDescriptorSetAllocateInfo ai = { VK_STRUCTURE_TYPE_DESCRIPTOR_SET_ALLOCATE_INFO };
    ai.descriptorPool = res.descriptorPool;
    ai.descriptorSetCount = 4;
    ai.pSetLayouts = layouts;
    VkDescriptorSet sets[4];
    VK_CHECK(vkAllocateDescriptorSets(ctx.device, &ai, sets));
    res.shadowDS  = sets[0];
    res.geomDS    = sets[1];
    res.overlayDS = sets[2];
    res.computeDS = sets[3];

    // Update shadow DS (UBO dynamic)
    {
        VkDescriptorBufferInfo bufInfo = { res.uboBuffer, 0, sizeof(UniformData) };
        VkWriteDescriptorSet w = { VK_STRUCTURE_TYPE_WRITE_DESCRIPTOR_SET };
        w.dstSet = res.shadowDS;
        w.dstBinding = 0;
        w.descriptorCount = 1;
        w.descriptorType = VK_DESCRIPTOR_TYPE_UNIFORM_BUFFER_DYNAMIC;
        w.pBufferInfo = &bufInfo;
        vkUpdateDescriptorSets(ctx.device, 1, &w, 0, nullptr);
    }
    // Update geom DS (UBO dynamic + checker sampler)
    {
        VkDescriptorBufferInfo bufInfo = { res.uboBuffer, 0, sizeof(UniformData) };
        VkDescriptorImageInfo imgInfo = { res.sampler, res.checkerView, VK_IMAGE_LAYOUT_SHADER_READ_ONLY_OPTIMAL };
        VkWriteDescriptorSet writes[2] = {};
        writes[0].sType = VK_STRUCTURE_TYPE_WRITE_DESCRIPTOR_SET;
        writes[0].dstSet = res.geomDS;
        writes[0].dstBinding = 0;
        writes[0].descriptorCount = 1;
        writes[0].descriptorType = VK_DESCRIPTOR_TYPE_UNIFORM_BUFFER_DYNAMIC;
        writes[0].pBufferInfo = &bufInfo;
        writes[1].sType = VK_STRUCTURE_TYPE_WRITE_DESCRIPTOR_SET;
        writes[1].dstSet = res.geomDS;
        writes[1].dstBinding = 1;
        writes[1].descriptorCount = 1;
        writes[1].descriptorType = VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER;
        writes[1].pImageInfo = &imgInfo;
        vkUpdateDescriptorSets(ctx.device, 2, writes, 0, nullptr);
    }
    // Update overlay DS (gradient sampler)
    {
        VkDescriptorImageInfo imgInfo = { res.sampler, res.gradientView, VK_IMAGE_LAYOUT_SHADER_READ_ONLY_OPTIMAL };
        VkWriteDescriptorSet w = { VK_STRUCTURE_TYPE_WRITE_DESCRIPTOR_SET };
        w.dstSet = res.overlayDS;
        w.dstBinding = 0;
        w.descriptorCount = 1;
        w.descriptorType = VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER;
        w.pImageInfo = &imgInfo;
        vkUpdateDescriptorSets(ctx.device, 1, &w, 0, nullptr);
    }
    // Update compute DS (geomColor as sampled input, computeOutput as storage)
    {
        VkDescriptorImageInfo sampledInfo = { res.sampler, res.geomColorView, VK_IMAGE_LAYOUT_SHADER_READ_ONLY_OPTIMAL };
        VkDescriptorImageInfo storageInfo = { VK_NULL_HANDLE, res.computeOutputView, VK_IMAGE_LAYOUT_GENERAL };
        VkWriteDescriptorSet writes[2] = {};
        writes[0].sType = VK_STRUCTURE_TYPE_WRITE_DESCRIPTOR_SET;
        writes[0].dstSet = res.computeDS;
        writes[0].dstBinding = 0;
        writes[0].descriptorCount = 1;
        writes[0].descriptorType = VK_DESCRIPTOR_TYPE_COMBINED_IMAGE_SAMPLER;
        writes[0].pImageInfo = &sampledInfo;
        writes[1].sType = VK_STRUCTURE_TYPE_WRITE_DESCRIPTOR_SET;
        writes[1].dstSet = res.computeDS;
        writes[1].dstBinding = 1;
        writes[1].descriptorCount = 1;
        writes[1].descriptorType = VK_DESCRIPTOR_TYPE_STORAGE_IMAGE;
        writes[1].pImageInfo = &storageInfo;
        vkUpdateDescriptorSets(ctx.device, 2, writes, 0, nullptr);
    }
}

