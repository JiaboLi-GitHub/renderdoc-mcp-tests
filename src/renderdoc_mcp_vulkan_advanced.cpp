static void cleanup(VulkanContext& ctx, RenderResources& res) {
    vkDeviceWaitIdle(ctx.device);

    vkDestroyDescriptorPool(ctx.device, presentDescPool, nullptr);

    vkDestroyPipeline(ctx.device, res.computePipeline, nullptr);
    vkDestroyPipeline(ctx.device, res.presentPipeline, nullptr);
    vkDestroyPipeline(ctx.device, res.overlayPipeline, nullptr);
    vkDestroyPipeline(ctx.device, res.geomCubePipeline, nullptr);
    vkDestroyPipeline(ctx.device, res.shadowPipeline, nullptr);

    vkDestroyPipelineLayout(ctx.device, res.computePipeLayout, nullptr);
    vkDestroyPipelineLayout(ctx.device, res.presentPipeLayout, nullptr);
    vkDestroyPipelineLayout(ctx.device, res.overlayPipeLayout, nullptr);
    vkDestroyPipelineLayout(ctx.device, res.geomPipeLayout, nullptr);
    vkDestroyPipelineLayout(ctx.device, res.shadowPipeLayout, nullptr);

    vkDestroyDescriptorPool(ctx.device, res.descriptorPool, nullptr);
    vkDestroyDescriptorSetLayout(ctx.device, res.computeDSLayout, nullptr);
    vkDestroyDescriptorSetLayout(ctx.device, res.overlayDSLayout, nullptr);
    vkDestroyDescriptorSetLayout(ctx.device, res.geomDSLayout, nullptr);
    vkDestroyDescriptorSetLayout(ctx.device, res.shadowDSLayout, nullptr);

    for (auto fb : res.presentFramebuffers) vkDestroyFramebuffer(ctx.device, fb, nullptr);
    vkDestroyFramebuffer(ctx.device, res.geomFramebuffer, nullptr);
    vkDestroyFramebuffer(ctx.device, res.shadowFramebuffer, nullptr);

    vkDestroyRenderPass(ctx.device, res.presentRenderPass, nullptr);
    vkDestroyRenderPass(ctx.device, res.geomRenderPass, nullptr);
    vkDestroyRenderPass(ctx.device, res.shadowRenderPass, nullptr);

    vkDestroyImageView(ctx.device, res.computeOutputView, nullptr);
    vkDestroyImage(ctx.device, res.computeOutputImage, nullptr);
    vkFreeMemory(ctx.device, res.computeOutputMem, nullptr);

    vkDestroyImageView(ctx.device, res.geomDepthView, nullptr);
    vkDestroyImage(ctx.device, res.geomDepthImage, nullptr);
    vkFreeMemory(ctx.device, res.geomDepthMem, nullptr);

    vkDestroyImageView(ctx.device, res.geomColorView, nullptr);
    vkDestroyImage(ctx.device, res.geomColorImage, nullptr);
    vkFreeMemory(ctx.device, res.geomColorMem, nullptr);

    vkDestroyImageView(ctx.device, res.shadowDepthView, nullptr);
    vkDestroyImage(ctx.device, res.shadowDepthImage, nullptr);
    vkFreeMemory(ctx.device, res.shadowDepthMem, nullptr);

    vkDestroySampler(ctx.device, res.sampler, nullptr);
    vkDestroyImageView(ctx.device, res.gradientView, nullptr);
    vkDestroyImage(ctx.device, res.gradientImage, nullptr);
    vkFreeMemory(ctx.device, res.gradientMem, nullptr);
    vkDestroyImageView(ctx.device, res.checkerView, nullptr);
    vkDestroyImage(ctx.device, res.checkerImage, nullptr);
    vkFreeMemory(ctx.device, res.checkerMem, nullptr);

    vkUnmapMemory(ctx.device, res.uboMem);
    vkDestroyBuffer(ctx.device, res.uboBuffer, nullptr);
    vkFreeMemory(ctx.device, res.uboMem, nullptr);

    vkDestroyBuffer(ctx.device, res.indexBuffer, nullptr);
    vkFreeMemory(ctx.device, res.indexMem, nullptr);
    vkDestroyBuffer(ctx.device, res.vertexBuffer, nullptr);
    vkFreeMemory(ctx.device, res.vertexMem, nullptr);

    vkDestroySemaphore(ctx.device, ctx.renderDoneSem, nullptr);
    vkDestroySemaphore(ctx.device, ctx.imageAvailSem, nullptr);
    vkDestroyFence(ctx.device, ctx.fence, nullptr);
    vkDestroyCommandPool(ctx.device, ctx.commandPool, nullptr);

    for (auto v : ctx.swapchainViews) vkDestroyImageView(ctx.device, v, nullptr);
    vkDestroySwapchainKHR(ctx.device, ctx.swapchain, nullptr);
    vkDestroyDevice(ctx.device, nullptr);
    vkDestroySurfaceKHR(ctx.instance, ctx.surface, nullptr);
    vkDestroyInstance(ctx.instance, nullptr);
}

