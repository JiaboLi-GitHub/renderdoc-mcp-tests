static void createDevice(VulkanContext& ctx) {
    uint32_t qfCount = 0;
    vkGetPhysicalDeviceQueueFamilyProperties(ctx.physicalDevice, &qfCount, nullptr);
    std::vector<VkQueueFamilyProperties> qfs(qfCount);
    vkGetPhysicalDeviceQueueFamilyProperties(ctx.physicalDevice, &qfCount, qfs.data());

    ctx.graphicsFamily = UINT32_MAX;
    for (uint32_t i = 0; i < qfCount; i++) {
        if (qfs[i].queueFlags & (VK_QUEUE_GRAPHICS_BIT | VK_QUEUE_COMPUTE_BIT)) {
            VkBool32 present = VK_FALSE;
            vkGetPhysicalDeviceSurfaceSupportKHR(ctx.physicalDevice, i, ctx.surface, &present);
            if (present) { ctx.graphicsFamily = i; break; }
        }
    }
    if (ctx.graphicsFamily == UINT32_MAX) { fprintf(stderr, "No suitable queue family\n"); exit(1); }

    float priority = 1.0f;
    VkDeviceQueueCreateInfo qci = { VK_STRUCTURE_TYPE_DEVICE_QUEUE_CREATE_INFO };
    qci.queueFamilyIndex = ctx.graphicsFamily;
    qci.queueCount = 1;
    qci.pQueuePriorities = &priority;

    const char* ext = VK_KHR_SWAPCHAIN_EXTENSION_NAME;
    VkDeviceCreateInfo dci = { VK_STRUCTURE_TYPE_DEVICE_CREATE_INFO };
    dci.queueCreateInfoCount = 1;
    dci.pQueueCreateInfos = &qci;
    dci.enabledExtensionCount = 1;
    dci.ppEnabledExtensionNames = &ext;

    VK_CHECK(vkCreateDevice(ctx.physicalDevice, &dci, nullptr, &ctx.device));
    vkGetDeviceQueue(ctx.device, ctx.graphicsFamily, 0, &ctx.graphicsQueue);
}

static void createSwapchain(VulkanContext& ctx) {
    VkSurfaceCapabilitiesKHR caps;
    vkGetPhysicalDeviceSurfaceCapabilitiesKHR(ctx.physicalDevice, ctx.surface, &caps);

    uint32_t fmtCount = 0;
    vkGetPhysicalDeviceSurfaceFormatsKHR(ctx.physicalDevice, ctx.surface, &fmtCount, nullptr);
    std::vector<VkSurfaceFormatKHR> fmts(fmtCount);
    vkGetPhysicalDeviceSurfaceFormatsKHR(ctx.physicalDevice, ctx.surface, &fmtCount, fmts.data());
    ctx.swapchainFormat = fmts[0].format;
    VkColorSpaceKHR colorSpace = fmts[0].colorSpace;
    for (auto& f : fmts) {
        if (f.format == VK_FORMAT_B8G8R8A8_UNORM) {
            ctx.swapchainFormat = f.format;
            colorSpace = f.colorSpace;
            break;
        }
    }

    uint32_t imageCount = caps.minImageCount + 1;
    if (caps.maxImageCount > 0 && imageCount > caps.maxImageCount)
        imageCount = caps.maxImageCount;

    VkSwapchainCreateInfoKHR ci = { VK_STRUCTURE_TYPE_SWAPCHAIN_CREATE_INFO_KHR };
    ci.surface = ctx.surface;
    ci.minImageCount = imageCount;
    ci.imageFormat = ctx.swapchainFormat;
    ci.imageColorSpace = colorSpace;
    ci.imageExtent = { WINDOW_WIDTH, WINDOW_HEIGHT };
    ci.imageArrayLayers = 1;
    ci.imageUsage = VK_IMAGE_USAGE_COLOR_ATTACHMENT_BIT;
    ci.imageSharingMode = VK_SHARING_MODE_EXCLUSIVE;
    ci.preTransform = caps.currentTransform;
    ci.compositeAlpha = VK_COMPOSITE_ALPHA_OPAQUE_BIT_KHR;
    ci.presentMode = VK_PRESENT_MODE_FIFO_KHR;
    ci.clipped = VK_TRUE;

    VK_CHECK(vkCreateSwapchainKHR(ctx.device, &ci, nullptr, &ctx.swapchain));

    vkGetSwapchainImagesKHR(ctx.device, ctx.swapchain, &ctx.swapchainImageCount, nullptr);
    ctx.swapchainImages.resize(ctx.swapchainImageCount);
    vkGetSwapchainImagesKHR(ctx.device, ctx.swapchain, &ctx.swapchainImageCount, ctx.swapchainImages.data());

    ctx.swapchainViews.resize(ctx.swapchainImageCount);
    for (uint32_t i = 0; i < ctx.swapchainImageCount; i++)
        ctx.swapchainViews[i] = createImageView(ctx.device, ctx.swapchainImages[i], ctx.swapchainFormat, VK_IMAGE_ASPECT_COLOR_BIT);
}

