static void createTextures(VulkanContext& ctx, RenderResources& res) {
    // Checker
    std::vector<uint8_t> checkerPixels;
    buildCheckerTexture(checkerPixels);
    createImage(ctx.device, ctx.memProps, TEX_SIZE, TEX_SIZE, VK_FORMAT_R8G8B8A8_UNORM,
                VK_IMAGE_USAGE_SAMPLED_BIT | VK_IMAGE_USAGE_TRANSFER_DST_BIT,
                res.checkerImage, res.checkerMem);
    res.checkerView = createImageView(ctx.device, res.checkerImage, VK_FORMAT_R8G8B8A8_UNORM, VK_IMAGE_ASPECT_COLOR_BIT);
    uploadTexture(ctx.device, ctx.memProps, ctx.commandPool, ctx.graphicsQueue,
                  res.checkerImage, TEX_SIZE, TEX_SIZE, checkerPixels.data());

    // Gradient
    std::vector<uint8_t> gradientPixels;
    buildGradientTexture(gradientPixels);
    createImage(ctx.device, ctx.memProps, TEX_SIZE, TEX_SIZE, VK_FORMAT_R8G8B8A8_UNORM,
                VK_IMAGE_USAGE_SAMPLED_BIT | VK_IMAGE_USAGE_TRANSFER_DST_BIT,
                res.gradientImage, res.gradientMem);
    res.gradientView = createImageView(ctx.device, res.gradientImage, VK_FORMAT_R8G8B8A8_UNORM, VK_IMAGE_ASPECT_COLOR_BIT);
    uploadTexture(ctx.device, ctx.memProps, ctx.commandPool, ctx.graphicsQueue,
                  res.gradientImage, TEX_SIZE, TEX_SIZE, gradientPixels.data());

    // Sampler
    VkSamplerCreateInfo sci = { VK_STRUCTURE_TYPE_SAMPLER_CREATE_INFO };
    sci.magFilter = VK_FILTER_LINEAR;
    sci.minFilter = VK_FILTER_LINEAR;
    sci.mipmapMode = VK_SAMPLER_MIPMAP_MODE_LINEAR;
    sci.addressModeU = VK_SAMPLER_ADDRESS_MODE_REPEAT;
    sci.addressModeV = VK_SAMPLER_ADDRESS_MODE_REPEAT;
    sci.addressModeW = VK_SAMPLER_ADDRESS_MODE_REPEAT;
    sci.maxLod = 1.0f;
    VK_CHECK(vkCreateSampler(ctx.device, &sci, nullptr, &res.sampler));
}

static void createOffscreenImages(VulkanContext& ctx, RenderResources& res) {
    // Shadow depth 512x512
    createImage(ctx.device, ctx.memProps, SHADOW_SIZE, SHADOW_SIZE, VK_FORMAT_D32_SFLOAT,
                VK_IMAGE_USAGE_DEPTH_STENCIL_ATTACHMENT_BIT,
                res.shadowDepthImage, res.shadowDepthMem);
    res.shadowDepthView = createImageView(ctx.device, res.shadowDepthImage, VK_FORMAT_D32_SFLOAT, VK_IMAGE_ASPECT_DEPTH_BIT);

    // Geometry color
    createImage(ctx.device, ctx.memProps, WINDOW_WIDTH, WINDOW_HEIGHT, VK_FORMAT_R8G8B8A8_UNORM,
                VK_IMAGE_USAGE_COLOR_ATTACHMENT_BIT | VK_IMAGE_USAGE_SAMPLED_BIT,
                res.geomColorImage, res.geomColorMem);
    res.geomColorView = createImageView(ctx.device, res.geomColorImage, VK_FORMAT_R8G8B8A8_UNORM, VK_IMAGE_ASPECT_COLOR_BIT);

    // Geometry depth
    createImage(ctx.device, ctx.memProps, WINDOW_WIDTH, WINDOW_HEIGHT, VK_FORMAT_D32_SFLOAT,
                VK_IMAGE_USAGE_DEPTH_STENCIL_ATTACHMENT_BIT,
                res.geomDepthImage, res.geomDepthMem);
    res.geomDepthView = createImageView(ctx.device, res.geomDepthImage, VK_FORMAT_D32_SFLOAT, VK_IMAGE_ASPECT_DEPTH_BIT);

    // Compute output
    createImage(ctx.device, ctx.memProps, WINDOW_WIDTH, WINDOW_HEIGHT, VK_FORMAT_R8G8B8A8_UNORM,
                VK_IMAGE_USAGE_STORAGE_BIT | VK_IMAGE_USAGE_SAMPLED_BIT,
                res.computeOutputImage, res.computeOutputMem);
    res.computeOutputView = createImageView(ctx.device, res.computeOutputImage, VK_FORMAT_R8G8B8A8_UNORM, VK_IMAGE_ASPECT_COLOR_BIT);

    // Transition compute output to GENERAL for initial use
    VkCommandBuffer cb = beginSingleTimeCommands(ctx.device, ctx.commandPool);
    transitionImageLayout(cb, res.computeOutputImage, VK_IMAGE_LAYOUT_UNDEFINED, VK_IMAGE_LAYOUT_GENERAL,
                          VK_IMAGE_ASPECT_COLOR_BIT,
                          VK_PIPELINE_STAGE_TOP_OF_PIPE_BIT, VK_PIPELINE_STAGE_COMPUTE_SHADER_BIT,
                          0, VK_ACCESS_SHADER_WRITE_BIT);
    endSingleTimeCommands(ctx.device, ctx.commandPool, ctx.graphicsQueue, cb);
}

