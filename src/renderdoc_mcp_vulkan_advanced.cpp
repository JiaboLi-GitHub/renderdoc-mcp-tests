// 24 vertices, 36 indices for a cube
static void buildCubeData(std::vector<Vertex>& verts, std::vector<uint16_t>& indices) {
    // Face order: +X, -X, +Y, -Y, +Z, -Z
    struct FaceDef { float nx, ny, nz; float ax, ay, az; float bx, by, bz; };
    FaceDef faces[6] = {
        { 1, 0, 0,  0, 1, 0,  0, 0,-1}, // +X
        {-1, 0, 0,  0, 1, 0,  0, 0, 1}, // -X
        { 0, 1, 0,  1, 0, 0,  0, 0, 1}, // +Y
        { 0,-1, 0,  1, 0, 0,  0, 0,-1}, // -Y
        { 0, 0, 1,  1, 0, 0,  0, 1, 0}, // +Z
        { 0, 0,-1, -1, 0, 0,  0, 1, 0}, // -Z
    };
    verts.resize(24);
    indices.resize(36);
    for (int f = 0; f < 6; f++) {
        auto& fd = faces[f];
        // center = normal direction
        float cx = fd.nx, cy = fd.ny, cz = fd.nz;
        // 4 corners: center +/- a +/- b
        float corners[4][3];
        for (int c = 0; c < 4; c++) {
            float sa = (c & 1) ? 1.0f : -1.0f;
            float sb = (c & 2) ? 1.0f : -1.0f;
            corners[c][0] = cx + sa * fd.ax + sb * fd.bx;
            corners[c][1] = cy + sa * fd.ay + sb * fd.by;
            corners[c][2] = cz + sa * fd.az + sb * fd.bz;
        }
        float uvs[4][2] = {{0,0},{1,0},{0,1},{1,1}};
        for (int v = 0; v < 4; v++) {
            Vertex& vt = verts[f * 4 + v];
            vt.pos[0] = corners[v][0]; vt.pos[1] = corners[v][1]; vt.pos[2] = corners[v][2];
            vt.normal[0] = fd.nx; vt.normal[1] = fd.ny; vt.normal[2] = fd.nz;
            vt.texcoord[0] = uvs[v][0]; vt.texcoord[1] = uvs[v][1];
        }
        uint16_t base = (uint16_t)(f * 4);
        indices[f * 6 + 0] = base + 0;
        indices[f * 6 + 1] = base + 1;
        indices[f * 6 + 2] = base + 2;
        indices[f * 6 + 3] = base + 2;
        indices[f * 6 + 4] = base + 1;
        indices[f * 6 + 5] = base + 3;
    }
}

