static void createVertexIndexBuffers(VulkanContext& ctx, RenderResources& res) {
    std::vector<Vertex> verts;
    std::vector<uint16_t> indices;
    buildCubeData(verts, indices);

    VkDeviceSize vSize = verts.size() * sizeof(Vertex);
    VkDeviceSize iSize = indices.size() * sizeof(uint16_t);

    // Create staging, copy, then create device-local
    VkBuffer stagingV, stagingI;
    VkDeviceMemory stagingVMem, stagingIMem;
    createBuffer(ctx.device, ctx.memProps, vSize, VK_BUFFER_USAGE_TRANSFER_SRC_BIT,
                 VK_MEMORY_PROPERTY_HOST_VISIBLE_BIT | VK_MEMORY_PROPERTY_HOST_COHERENT_BIT, stagingV, stagingVMem);
    createBuffer(ctx.device, ctx.memProps, iSize, VK_BUFFER_USAGE_TRANSFER_SRC_BIT,
                 VK_MEMORY_PROPERTY_HOST_VISIBLE_BIT | VK_MEMORY_PROPERTY_HOST_COHERENT_BIT, stagingI, stagingIMem);

    void* mapped;
    vkMapMemory(ctx.device, stagingVMem, 0, vSize, 0, &mapped);
    memcpy(mapped, verts.data(), (size_t)vSize);
    vkUnmapMemory(ctx.device, stagingVMem);

    vkMapMemory(ctx.device, stagingIMem, 0, iSize, 0, &mapped);
    memcpy(mapped, indices.data(), (size_t)iSize);
    vkUnmapMemory(ctx.device, stagingIMem);

    createBuffer(ctx.device, ctx.memProps, vSize,
                 VK_BUFFER_USAGE_VERTEX_BUFFER_BIT | VK_BUFFER_USAGE_TRANSFER_DST_BIT,
                 VK_MEMORY_PROPERTY_DEVICE_LOCAL_BIT, res.vertexBuffer, res.vertexMem);
    createBuffer(ctx.device, ctx.memProps, iSize,
                 VK_BUFFER_USAGE_INDEX_BUFFER_BIT | VK_BUFFER_USAGE_TRANSFER_DST_BIT,
                 VK_MEMORY_PROPERTY_DEVICE_LOCAL_BIT, res.indexBuffer, res.indexMem);

    VkCommandBuffer cb = beginSingleTimeCommands(ctx.device, ctx.commandPool);
    VkBufferCopy copyV = { 0, 0, vSize };
    vkCmdCopyBuffer(cb, stagingV, res.vertexBuffer, 1, &copyV);
    VkBufferCopy copyI = { 0, 0, iSize };
    vkCmdCopyBuffer(cb, stagingI, res.indexBuffer, 1, &copyI);
    endSingleTimeCommands(ctx.device, ctx.commandPool, ctx.graphicsQueue, cb);

    vkDestroyBuffer(ctx.device, stagingV, nullptr);
    vkFreeMemory(ctx.device, stagingVMem, nullptr);
    vkDestroyBuffer(ctx.device, stagingI, nullptr);
    vkFreeMemory(ctx.device, stagingIMem, nullptr);
}

