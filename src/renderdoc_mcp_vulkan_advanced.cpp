static void buildCheckerTexture(std::vector<uint8_t>& pixels) {
    pixels.resize(TEX_SIZE * TEX_SIZE * 4);
    for (uint32_t y = 0; y < TEX_SIZE; y++) {
        for (uint32_t x = 0; x < TEX_SIZE; x++) {
            bool white = ((x / 8) + (y / 8)) % 2 == 0;
            uint8_t* p = &pixels[(y * TEX_SIZE + x) * 4];
            if (white) { p[0] = 255; p[1] = 0; p[2] = 255; p[3] = 255; } // magenta
            else       { p[0] = 40;  p[1] = 20; p[2] = 40;  p[3] = 255; } // dark
        }
    }
}

static void buildGradientTexture(std::vector<uint8_t>& pixels) {
    pixels.resize(TEX_SIZE * TEX_SIZE * 4);
    for (uint32_t y = 0; y < TEX_SIZE; y++) {
        float t = (float)y / (float)(TEX_SIZE - 1);
        float alpha = 1.0f - 0.7f * t; // 1.0 -> 0.3
        for (uint32_t x = 0; x < TEX_SIZE; x++) {
            float s = (float)x / (float)(TEX_SIZE - 1);
            uint8_t* p = &pixels[(y * TEX_SIZE + x) * 4];
            // orange (255,165,0) -> cyan (0,255,255)
            p[0] = (uint8_t)(255.0f * (1.0f - s) + 0.0f * s);
            p[1] = (uint8_t)(165.0f * (1.0f - s) + 255.0f * s);
            p[2] = (uint8_t)(0.0f * (1.0f - s) + 255.0f * s);
            p[3] = (uint8_t)(alpha * 255.0f);
        }
    }
}

